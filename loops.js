// Loops in Javascripts

//This loop counts from 0 to 10.

for (let i = 0; i <= 10; i++) {
  console.log(i);
}

// Do While loop
// Be careful with this one because, unlike while, it will always run at least once
// and then checks if the condition is true.
let j = 0;
do {
  j += 1;
  console.log(j);
} while (j <= 0);